x=5
y=10

print(f"Operators: {x}, {y}")

#--------------------------------------------------arithmetic operations
print("Arithmetic Operators")
print(f"Addition: {x+y}")
print(f"Substraction: {x-y}")
print(f"Multiplication: {x*y}")
print(f"Division: {x/y}")
print(f"Floor division: {x//y}")
print(f"Power: {x**y}")
print(f"Modulo: {x%y}")

#--------------------------------------------------logical operators
print("Logical Operators")
print(f"AND: {x and y}")
print(f"OR: {x or y}")
print(f"NOT(AND): {not(x and y)}")

#--------------------------------------------------relational operators
print("Relational Operators")
print(f"EQUAL TO: {x==y}")
print(f"NOT EQUAL TO: {x!=y}")
print(f"LESS THAN: {x<y}")
print(f"GREATER THAN: {x>y}")
print(f"LESS THAN and EQUAL TO: {x<=y}")
print(f"GREATER THAN and EQUAL TO: {x>=y}")